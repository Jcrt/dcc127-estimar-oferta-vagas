package model.estrutura;

public class DadosGerais {
	
	private String codigoCurso;
	private String dataUltimaImportacao;
	private Integer alunosTotaisCurso;
	private String gradeCurso;
	private Integer quantidadeAlunosGrade;
	
	
	
	public String getCodigoCurso() {
		return codigoCurso;
	}
	public void setCodigoCurso(String codigoCurso) {
		this.codigoCurso = codigoCurso;
	}
	public String getDataUltimaImportacao() {
		return dataUltimaImportacao;
	}
	public void setDataUltimaImportacao(String dataUltimaImportacao) {
		this.dataUltimaImportacao = dataUltimaImportacao;
	}
	public Integer getAlunosTotaisCurso() {
		return alunosTotaisCurso;
	}
	public void setAlunosTotaisCurso(Integer alunosTotaisCurso) {
		this.alunosTotaisCurso = alunosTotaisCurso;
	}
	public String getGradeCurso() {
		return gradeCurso;
	}
	public void setGradeCurso(String gradeCurso) {
		this.gradeCurso = gradeCurso;
	}
	public Integer getQuantidadeAlunosGrade() {
		return quantidadeAlunosGrade;
	}
	public void setQuantidadeAlunosGrade(Integer quantidadeAlunosGrade) {
		this.quantidadeAlunosGrade = quantidadeAlunosGrade;
	}
	
	

}
